let added = 0;
let skipped = 0;
for (const api of APIS) {
  const exists = await prisma.api.findUnique({ where: { slug: api.slug } });
  if (exists) {
    console.log('skip', api.slug);
    skipped++;
    continue;
  }

  await prisma.api.create({
    data: {
      slug: api.slug,
      name: api.name,
      category: api.category,
      icon: api.icon,
      shortDescription: api.shortDescription,
      fullDescription: api.fullDescription,
      useCases: api.useCases,
      documentation: api.documentation,
      baseUrl: api.baseUrl,
      providerAuthHeader: api.providerAuthHeader || null,
      providerAuthScheme: api.providerAuthScheme || null,
      allowUnauthenticated: api.allowUnauthenticated,
      authentication: api.authentication,
      rateLimit: api.rateLimit,
      pricing: api.pricing,
      webhook: false,
    },
  });
  console.log('add ', api.slug);
  added++;
}

const total = await prisma.api.count();
console.log(`\nAdded ${added}, skipped ${skipped}. Total APIs: ${total}`);

await prisma.$disconnect();
