async function main() {
  console.log('Seeding new APIs...');
  
  let created = 0;
  let skipped = 0;

  for (const apiData of newApis) {
    const { endpoints, ...apiFields } = apiData;
    
    // Skip if already exists
    const existing = await prisma.api.findUnique({ where: { slug: apiFields.slug } });
    if (existing) {
      console.log(`  ⏭ ${apiFields.name} (already exists)`);
      skipped++;
      continue;
    }

    const api = await prisma.api.create({
      data: {
        ...apiFields,
        endpoints: {
          create: endpoints,
        },
      },
    });
    console.log(`  ✓ ${api.name}`);
    created++;
  }

  console.log(`\nDone! Created ${created} APIs, skipped ${skipped}.`);
}

main()
  .catch(e => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
